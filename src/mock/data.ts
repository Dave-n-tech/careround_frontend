// ─── Vitals helper ───────────────────────────────────────────────────────────

type VB = {
  hr: number;
  resp: number;
  spo2: number;
  sys: number;
  temp: number;
  cons: "ALERT" | "VOICE" | "PAIN" | "UNRESPONSIVE";
};

function makeVitals(patientId: string, seed: number, base: VB): Vitals[] {
  const out: Vitals[] = [];
  let t = Date.now() - 36 * 3600_000;
  for (let i = 0; i < 14; i++) {
    const j = (n: number, r: number) => n + Math.sin(seed + i * 1.7) * r;
    const hr = Math.round(j(base.hr, 6));
    const resp = Math.round(j(base.resp, 2));
    const spo2 = Math.round(j(base.spo2, 1.5));
    const sys = Math.round(j(base.sys, 7));
    const temp = Number(j(base.temp, 0.4).toFixed(1));
    const news = computeNEWS({
      resp,
      spo2,
      temp,
      sys,
      hr,
      cons: base.cons,
    }).total;
    out.push({
      id: `v_${patientId}_${i}`,
      patientId,
      recordedById: "u_nur1",
      heartRate: hr,
      respiratoryRate: resp,
      oxygenSaturation: spo2,
      systolicBP: sys,
      temperature: temp,
      consciousnessLevel: base.cons,
      newsScore: news,
      recordedAt: new Date(t).toISOString(),
    });
    t += 2.5 * 3600_000;
  }
  return out;
}

